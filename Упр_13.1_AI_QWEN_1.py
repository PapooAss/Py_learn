# Заданная строка (например, отрывок стихотворения)
text = "Мороз и солнце день чудесный Еще ты дремлешь друг прелестный"

# Разделяем строку на список слов
words = text.split()

# Выводим информацию о количестве слов
print(f"В тексте {len(words)} слов.")

# Запрашиваем у пользователя номер слова
try:
    index = int(input("Введите номер слова, которое хотите вывести (начиная с 1): "))

    # Проверяем корректность введенного номера
    if 1 <= index <= len(words):
        # Выводим слово (учитываем, что индексация в списке начинается с 0)
        print(f"Слово под номером {index}: {words[index - 1]}")
    else:
        print("Ошибка: номер должен быть в диапазоне от 1 до", len(words))
except ValueError:
    print("Ошибка: введите целое число.")
